function getLegalLang() {
  return localStorage.getItem("adwork_lang") || "ca";
}

function tl(key) {
  const lang = getLegalLang();
  return legalTranslations[lang]?.[key] || key;
}

function applyLegalTranslations() {
  const lang = getLegalLang();
  document.documentElement.lang = lang;

  const currentLang = document.getElementById("currentLang");
  if (currentLang) currentLang.textContent = lang.toUpperCase();

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const text = tl(key);
    if (text && text !== key) {
      el.textContent = text;
    }
  });

  const isPrivacy = !!document.querySelector('[data-i18n="privacy_title"]');
  const isTerms = !!document.querySelector('[data-i18n="terms_title"]');

  if (isPrivacy) {
    document.title = lang === "ca"
      ? "Política de Privacitat - AdWork"
      : "Política de Privacidad - AdWork";
  }

  if (isTerms) {
    document.title = lang === "ca"
      ? "Termes i Condicions - AdWork"
      : "Términos y Condiciones - AdWork";
  }
}

const langToggle = document.getElementById("langToggle");
const langMenu = document.getElementById("langMenu");

langToggle?.addEventListener("click", (e) => {
  e.stopPropagation();
  langMenu?.classList.toggle("hidden");
});

document.querySelectorAll(".lang-option").forEach((btn) => {
  btn.addEventListener("click", (e) => {
    e.stopPropagation();
    const lang = btn.dataset.lang;
    if (!lang) return;

    localStorage.setItem("adwork_lang", lang);
    applyLegalTranslations();
    langMenu?.classList.add("hidden");
  });
});

document.addEventListener("click", (e) => {
  if (!e.target.closest(".lang-switcher")) {
    langMenu?.classList.add("hidden");
  }
});

applyLegalTranslations();